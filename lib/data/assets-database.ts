/**
 * Busca ativos por ticker ou nome
 */
export function searchAssets(query: string, limit: number = 10): AssetData[] {
  if (!query || query.length < 2) return [];

  const normalizedQuery = query.toLowerCase().trim();

  return ASSETS_DATABASE.filter(
    (asset) =>
      asset.isActive &&
      (asset.ticker.toLowerCase().includes(normalizedQuery) ||
        asset.name.toLowerCase().includes(normalizedQuery)),
  )
    .sort((a, b) => {
      // Priorizar matches exatos no ticker
      const aTickerMatch = a.ticker.toLowerCase().startsWith(normalizedQuery);
      const bTickerMatch = b.ticker.toLowerCase().startsWith(normalizedQuery);

      if (aTickerMatch && !bTickerMatch) return -1;
      if (!aTickerMatch && bTickerMatch) return 1;

      // Depois por tamanho do ticker (mais curto primeiro)
      return a.ticker.length - b.ticker.length;
    })
    .slice(0, limit);
}

/**
 * Busca ativo por ticker exato
 */
export function getAssetByTicker(ticker: string): AssetData | undefined {
  return ASSETS_DATABASE.find(
    (asset) =>
      asset.ticker.toLowerCase() === ticker.toLowerCase() && asset.isActive,
  );
}

/**
 * Busca ativos por tipo
 */
export function getAssetsByType(assetType: AssetType): AssetData[] {
  return ASSETS_DATABASE.filter(
    (asset) => asset.assetType === assetType && asset.isActive,
  );
}

/**
 * Adiciona um novo ativo customizado à base
 */
export function addCustomAsset(
  assetData: Omit<AssetData, "id" | "isActive">,
): AssetData {
  const newAsset: AssetData = {
    ...assetData,
    id: `CUSTOM_${assetData.ticker}_${Date.now()}`,
    isActive: true,
  };

  // Em produção, isso seria salvo no backend
  ASSETS_DATABASE.push(newAsset);

  return newAsset;
}
